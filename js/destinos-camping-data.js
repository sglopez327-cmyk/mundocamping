/** Convierte lat/lng a % en proyección equirectangular */
window.mcDestinoXY = function (lat, lng) {
  var x = ((lng + 180) / 360) * 100;
  var y = ((90 - lat) / 180) * 100;
  return { x: Math.min(98, Math.max(2, x)), y: Math.min(96, Math.max(4, y)) };
};
